package deneme;

public class mmm {

	    public static void main(String[] args) {
	       

	        // Her bir satır için döngü
	        for (int i = 1; i < 8; i+=2) {
	            
	            
	            // Yıldızları yazdırmak için döngü
	            for (int k = 0; k <= i-1; k++) {
	                System.out.print("*");
	            }
	            
	            
	            System.out.println();
	        }
	    }
	}
